%==========================================================================
% 2^15 = 32768 and the sum of its digits is 3 + 2 + 7 + 6 + 8 = 26.
% 
% What is the sum of the digits of the number 2^1000?
%==========================================================================
clear; close all;
%--------------------------------------------------------------------------
number = 2^1000;
array_number = num2str(number,'%.0f') - '0';
sum_of_digits = sum(array_number);
%--------------------------------------------------------------------------
fprintf('The answer is: %d\n',sum_of_digits)
%--------------------------------------------------------------------------


